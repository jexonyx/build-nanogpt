# simple launch:
# python train_gpt2.py
# DDP launch for e.g. 8 GPUs:
# torchrun --standalone --nproc_per_node=8 train_gpt2.py

def main(model_cls=GPT):
    # parse CLI arguments
    parser = argparse.ArgumentParser()
    parser.add_argument('--run-name', type=str, default='default', help='name for this run (determines log subdirectory)')
    parser.add_argument('--resume', type=str, default=None, help='checkpoint path to resume from')
    args = parser.parse_args()

    # run the training loop
    from torch.distributed import init_process_group, destroy_process_group
    from torch.nn.parallel import DistributedDataParallel as DDP
    import torch.distributed as dist

    # set up DDP (distributed data parallel).
    # torchrun command sets the env variables RANK, LOCAL_RANK, and WORLD_SIZE
    ddp = int(os.environ.get('RANK', -1)) != -1 # is this a ddp run?
    if ddp:
        # use of DDP atm demands CUDA, we set the device appropriately according to rank
        assert torch.cuda.is_available(), "for now i think we need CUDA for DDP"
        init_process_group(backend='nccl')
        ddp_rank = int(os.environ['RANK'])
        ddp_local_rank = int(os.environ['LOCAL_RANK'])
        ddp_world_size = int(os.environ['WORLD_SIZE'])
        device = f'cuda:{ddp_local_rank}'
        torch.cuda.set_device(device)
        master_process = ddp_rank == 0 # this process will do logging, checkpointing etc.
    else:
        # vanilla, non-DDP run
        ddp_rank = 0
        ddp_local_rank = 0
        ddp_world_size = 1
        master_process = True
        # attempt to autodetect device
        device = "cpu"
        if torch.cuda.is_available():
            device = "cuda"
        elif hasattr(torch.backends, "mps") and torch.backends.mps.is_available():
            device = "mps"
        print(f"using device: {device}")

    # added after video, pytorch can be serious about it's device vs. device_type distinction
    device_type = "cuda" if device.startswith("cuda") else "cpu"

    torch.manual_seed(1337)
    if torch.cuda.is_available():
        torch.cuda.manual_seed(1337)

    enc = tiktoken.get_encoding("gpt2")

    total_batch_size = 524288 # 2**19, ~0.5M, in number of tokens
    B = 64 # micro batch size
    T = 1024 # sequence length
    assert total_batch_size % (B * T * ddp_world_size) == 0, "make sure total_batch_size is divisible by B * T * ddp_world_size"
    grad_accum_steps = total_batch_size // (B * T * ddp_world_size)
    if master_process:
        print(f"total desired batch size: {total_batch_size}")
        print(f"=> calculated gradient accumulation steps: {grad_accum_steps}")

    train_loader = DataLoaderLite(B=B, T=T, process_rank=ddp_rank, num_processes=ddp_world_size, split="train", verbose=master_process)
    val_loader = DataLoaderLite(B=B, T=T, process_rank=ddp_rank, num_processes=ddp_world_size, split="val", verbose=master_process)

    torch.set_float32_matmul_precision('high')

    # create model
    model = model_cls(GPTConfig(vocab_size=50304))
    # model = GPT.from_pretrained("gpt2") # or init from OpenAI GPT-2
    model.to(device)
    use_compile = False # torch.compile interferes with HellaSwag eval and Generation. TODO fix
    if use_compile:
        model = torch.compile(model)
    if ddp:
        model = DDP(model, device_ids=[ddp_local_rank])
    raw_model = model.module if ddp else model # always contains the "raw" unwrapped model
    if master_process:
        total_params = sum(p.numel() for p in raw_model.parameters())
        print(f"total parameters: {total_params:,}")

    max_lr = 6e-4
    min_lr = max_lr * 0.1
    warmup_steps = 715
    max_steps = 19073 # 19,073 steps is ~1 epoch, if data is 10B tokens and batch size 0.5M tokens
    def get_lr(it):
        # 1) linear warmup for warmup_iters steps
        if it < warmup_steps:
            return max_lr * (it+1) / warmup_steps
        # 2) if it > lr_decay_iters, return min learning rate
        if it > max_steps:
            return min_lr
        # 3) in between, use cosine decay down to min learning rate
        decay_ratio = (it - warmup_steps) / (max_steps - warmup_steps)
        assert 0 <= decay_ratio <= 1
        coeff = 0.5 * (1.0 + math.cos(math.pi * decay_ratio)) # coeff starts at 1 and goes to 0
        return min_lr + coeff * (max_lr - min_lr)

    # optimize!
    optimizer = raw_model.configure_optimizers(weight_decay=0.1, learning_rate=6e-4, device_type=device_type, verbose=master_process)

    # checkpoint resumption
    start_step = 0
    if args.resume:
        if master_process:
            print(f"resuming from checkpoint: {args.resume}")
        checkpoint = torch.load(args.resume, map_location=device, weights_only=False)
        raw_model.load_state_dict(checkpoint['model'])
        optimizer.load_state_dict(checkpoint['optimizer'])
        torch.random.set_rng_state(checkpoint['rng_state'])
        if device_type == "cuda" and 'cuda_rng_state' in checkpoint:
            torch.cuda.set_rng_state(checkpoint['cuda_rng_state'])
        start_step = checkpoint['step'] + 1
        if master_process:
            print(f"resumed at step {start_step}")
        del checkpoint

    # activation norm hooks — gated by log_activations flag so they're no-ops most steps
    log_activations = False
    activation_norms = {}
    def make_activation_hook(layer_idx):
        def hook(module, input, output):
            if log_activations:
                activation_norms[layer_idx] = output.detach().norm().item()
        return hook
    for layer_idx, block in enumerate(raw_model.transformer.h):
        block.register_forward_hook(make_activation_hook(layer_idx))

    # create the log directory we will write checkpoints to and log to
    log_dir = os.path.join("log", args.run_name)
    os.makedirs(log_dir, exist_ok=True)
    log_file = os.path.join(log_dir, f"log.txt")
    metrics_file = os.path.join(log_dir, "metrics.jsonl")
    if not args.resume:
        with open(log_file, "w") as f: # open for writing to clear the file
            pass
        with open(metrics_file, "w") as f: # open for writing to clear the file
            pass

    for step in range(start_step, max_steps):
        t0 = time.time()
        last_step = (step == max_steps - 1)
        log_activations = (step % 50 == 0)
        activation_norms.clear()
        step_val_loss = None
        step_hella_acc = None

        # once in a while evaluate our validation loss
        if step % 250 == 0 or last_step:
            model.eval()
            val_loader.reset()
            with torch.no_grad():
                val_loss_accum = 0.0
                val_loss_steps = 20
                for _ in range(val_loss_steps):
                    x, y = val_loader.next_batch()
                    x, y = x.to(device), y.to(device)
                    with torch.autocast(device_type=device_type, dtype=torch.bfloat16):
                        logits, loss = model(x, y)
                    loss = loss / val_loss_steps
                    val_loss_accum += loss.detach()
            if ddp:
                dist.all_reduce(val_loss_accum, op=dist.ReduceOp.AVG)
            if master_process:
                step_val_loss = val_loss_accum.item()
                print(f"validation loss: {step_val_loss:.4f}")
                with open(log_file, "a") as f:
                    f.write(f"{step} val {step_val_loss:.4f}\n")
                if step > 0 and (step % 1000 == 0 or last_step):
                    # optionally write model checkpoints
                    checkpoint_path = os.path.join(log_dir, f"model_{step:05d}.pt")
                    checkpoint = {
                        'model': raw_model.state_dict(),
                        'config': vars(raw_model.config),
                        'step': step,
                        'val_loss': val_loss_accum.item(),
                        'optimizer': optimizer.state_dict(),
                        'rng_state': torch.random.get_rng_state(),
                    }
                    if device_type == "cuda":
                        checkpoint['cuda_rng_state'] = torch.cuda.get_rng_state()
                    torch.save(checkpoint, checkpoint_path)

        # once in a while evaluate hellaswag
        if (step % 250 == 0 or last_step) and (not use_compile):
            num_correct_norm = 0
            num_total = 0
            for i, example in enumerate(iterate_examples("val")):
                # only process examples where i % ddp_world_size == ddp_rank
                if i % ddp_world_size != ddp_rank:
                    continue
                # render the example into tokens and labels
                _, tokens, mask, label = render_example(example)
                tokens = tokens.to(device)
                mask = mask.to(device)
                # get the logits
                with torch.no_grad():
                    with torch.autocast(device_type=device_type, dtype=torch.bfloat16):
                        logits, loss = model(tokens)
                    pred_norm = get_most_likely_row(tokens, mask, logits)
                num_total += 1
                num_correct_norm += int(pred_norm == label)
            # reduce the stats across all processes
            if ddp:
                num_total = torch.tensor(num_total, dtype=torch.long, device=device)
                num_correct_norm = torch.tensor(num_correct_norm, dtype=torch.long, device=device)
                dist.all_reduce(num_total, op=dist.ReduceOp.SUM)
                dist.all_reduce(num_correct_norm, op=dist.ReduceOp.SUM)
                num_total = num_total.item()
                num_correct_norm = num_correct_norm.item()
            acc_norm = num_correct_norm / num_total
            if master_process:
                step_hella_acc = acc_norm
                print(f"HellaSwag accuracy: {num_correct_norm}/{num_total}={acc_norm:.4f}")
                with open(log_file, "a") as f:
                    f.write(f"{step} hella {acc_norm:.4f}\n")

        # once in a while generate from the model (except step 0, which is noise)
        if ((step > 0 and step % 250 == 0) or last_step) and (not use_compile):
            model.eval()
            num_return_sequences = 4
            max_length = 32
            tokens = enc.encode("Hello, I'm a language model,")
            tokens = torch.tensor(tokens, dtype=torch.long)
            tokens = tokens.unsqueeze(0).repeat(num_return_sequences, 1)
            xgen = tokens.to(device)
            sample_rng = torch.Generator(device=device)
            sample_rng.manual_seed(42 + ddp_rank)
            while xgen.size(1) < max_length:
                # forward the model to get the logits
                with torch.no_grad():
                    with torch.autocast(device_type=device_type, dtype=torch.bfloat16):
                        logits, loss = model(xgen) # (B, T, vocab_size)
                    # take the logits at the last position
                    logits = logits[:, -1, :] # (B, vocab_size)
                    # get the probabilities
                    probs = F.softmax(logits, dim=-1)
                    # do top-k sampling of 50 (huggingface pipeline default)
                    # topk_probs here becomes (5, 50), topk_indices is (5, 50)
                    topk_probs, topk_indices = torch.topk(probs, 50, dim=-1)
                    # select a token from the top-k probabilities
                    # note: multinomial does not demand the input to sum to 1
                    ix = torch.multinomial(topk_probs, 1, generator=sample_rng) # (B, 1)
                    # gather the corresponding indices
                    xcol = torch.gather(topk_indices, -1, ix) # (B, 1)
                    # append to the sequence
                    xgen = torch.cat((xgen, xcol), dim=1)
            # print the generated text
            for i in range(num_return_sequences):
                tokens = xgen[i, :max_length].tolist()
                decoded = enc.decode(tokens)
                print(f"rank {ddp_rank} sample {i}: {decoded}")

        # do one step of the optimization
        model.train()
        optimizer.zero_grad()
        loss_accum = 0.0
        for micro_step in range(grad_accum_steps):
            x, y = train_loader.next_batch()
            x, y = x.to(device), y.to(device)
            # added after video, this field is also used by the forward pass.
            if ddp:
                model.require_backward_grad_sync = (micro_step == grad_accum_steps - 1)
            with torch.autocast(device_type=device_type, dtype=torch.bfloat16):
                logits, loss = model(x, y)
            # we have to scale the loss to account for gradient accumulation,
            # because the gradients just add on each successive backward().
            # addition of gradients corresponds to a SUM in the objective, but
            # instead of a SUM we want MEAN. Scale the loss here so it comes out right
            loss = loss / grad_accum_steps
            loss_accum += loss.detach()
            loss.backward()
        if ddp:
            dist.all_reduce(loss_accum, op=dist.ReduceOp.AVG)
        norm = torch.nn.utils.clip_grad_norm_(model.parameters(), 1.0)
        # determine and set the learning rate for this iteration
        lr = get_lr(step)
        for param_group in optimizer.param_groups:
            param_group['lr'] = lr
        # snapshot per-block parameters every 50 steps for update-to-weight ratios
        param_snapshots = None
        if step % 50 == 0:
            param_snapshots = {}
            for layer_idx, block in enumerate(raw_model.transformer.h):
                params = torch.cat([p.data.detach().reshape(-1) for p in block.parameters()])
                param_snapshots[layer_idx] = params.clone()
        optimizer.step()
        # compute update-to-weight ratios
        update_ratios = None
        if param_snapshots is not None:
            update_ratios = {}
            for layer_idx, block in enumerate(raw_model.transformer.h):
                new_params = torch.cat([p.data.detach().reshape(-1) for p in block.parameters()])
                old_params = param_snapshots[layer_idx]
                update_ratios[layer_idx] = (new_params - old_params).norm().item() / old_params.norm().item()
            del param_snapshots
        if device_type == "cuda":
            torch.cuda.synchronize() # wait for the GPU to finish work
        t1 = time.time()
        dt = t1 - t0 # time difference in seconds
        tokens_processed = train_loader.B * train_loader.T * grad_accum_steps * ddp_world_size
        tokens_per_sec = tokens_processed / dt
        if master_process:
            print(f"step {step:5d} | loss: {loss_accum.item():.6f} | lr {lr:.4e} | norm: {norm:.4f} | dt: {dt*1000:.2f}ms | tok/sec: {tokens_per_sec:.2f}")
            # GPU memory
            gpu_mem_gb = torch.cuda.memory_allocated() / 1e9 if device_type == "cuda" else 0.0
            # per-layer gradient norms and weight norms
            layer_grad_norms = []
            layer_weight_norms = []
            for block in raw_model.transformer.h:
                grad_sq = sum(p.grad.norm().item()**2 for p in block.parameters() if p.grad is not None)
                weight_sq = sum(p.data.norm().item()**2 for p in block.parameters())
                layer_grad_norms.append(grad_sq**0.5)
                layer_weight_norms.append(weight_sq**0.5)
            # log.txt — expanded with lr, norm, timing, gpu mem
            with open(log_file, "a") as f:
                f.write(f"{step} train {loss_accum.item():.6f} lr {lr:.4e} norm {norm:.4f} dt {dt*1000:.2f} tok/sec {tokens_per_sec:.2f} gpu_mem {gpu_mem_gb:.2f}\n")
            # metrics.jsonl — one JSON object per step
            metrics = {
                "step": step,
                "train_loss": loss_accum.item(),
                "lr": lr,
                "grad_norm": norm.item() if hasattr(norm, 'item') else norm,
                "dt_ms": dt * 1000,
                "tok_sec": tokens_per_sec,
                "gpu_mem_gb": gpu_mem_gb,
                "layer_grad_norms": layer_grad_norms,
                "layer_weight_norms": layer_weight_norms,
            }
            if step_val_loss is not None:
                metrics["val_loss"] = step_val_loss
            if step_hella_acc is not None:
                metrics["hella_acc"] = step_hella_acc
            if update_ratios is not None:
                metrics["layer_update_ratios"] = [update_ratios[i] for i in range(len(raw_model.transformer.h))]
            if activation_norms:
                metrics["layer_act_norms"] = [activation_norms.get(i, 0.0) for i in range(len(raw_model.transformer.h))]
            with open(metrics_file, "a") as f:
                f.write(json.dumps(metrics) + "\n")

    if ddp:
        destroy_process_group()

if __name__ == "__main__":
    main()
